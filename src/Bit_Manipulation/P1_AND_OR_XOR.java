package Bit_Manipulation;

public class P1_AND_OR_XOR {
    public static void main(String[] args) {
        System.out.println(5 & 6);   // AND OPERATOR Both 1 is required  Ex.  1 1 = 1
        System.out.println(5 | 6);   // OR OPERATOR  Only 1 is required Ex. 1 0 = 1
        System.out.println(2 ^ 3);   // XOR OPERATOR Both same then Ex. 1 1 = 0

        System.out.println(~11);     // Binary One's Complement

        System.out.println(5<<2);
        System.out.println(5>>2);



    }
}
